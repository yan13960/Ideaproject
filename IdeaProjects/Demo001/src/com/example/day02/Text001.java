public class Text001 { //单行注释
    /*
      多行注释
      大写以后写多了就知道了
      在开发项目的时候一定要写注释
      我们在做作业的时候一定要养成习惯写注释
     */

    /**
     * 这个通常使用在类上
     * 还有方法上 以后课程会经常用
     * 现在只需要记住就可以了，面试也不会问
     */

    //这个不要记，主入口程序
    public static void main(String[] args) {
        System.out.println(123);//打印输出数字，可以不用加"" ''
        //如果打印输出字符串必需  "" 双引号  所有符号必 需在英文状态输入
        System.out.println("我爱学JAVA，我要跟曾老师学JAVA,高薪就业1");


        System.out.println("我爱学         JAVA，我要跟曾老师学JAVA,高薪就业2");
        //\t  必需在  ""号中  必需是\
        System.out.println("我爱学\tJAVA，我要跟曾老师学JAVA,高薪就业3");

        System.out.println("我爱学\nJAVA，我要跟曾老师学JAVA,高薪就业3");

        //学个就是就是完在作业的时候，需要排版，好看一点点

    }
}
